'''Question 1
You have the expression 5 + 3 * 2. Without using parentheses, 
calculate the result and then use parentheses to explicitly show the precedence.
'''
# Without using parentheses
result = 5 + 3 * 2
print(result)  

# With parentheses to explicitly show the precedence
result = 5 + (3 * 2)
print(result)  


